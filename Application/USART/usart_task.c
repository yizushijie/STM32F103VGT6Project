///////////////////////////////////////////////////////////////////////////////
//////函		数：
//////功		能：
//////输入参数:
//////输出参数:
//////说		明：
//////////////////////////////////////////////////////////////////////////////
UINT8_T USARTTask_FuncTask(USART_HandlerType*USARTx, UINT8_T(*pFuncTask)(UINT8_T *, UINT8_T *))
{
	UINT16_T length = 0;
	if (USARTx != NULL)
	{
		//---判断接收是否完成
		if (USARTTask_GetReadState(USARTx) == 1)
		{
			//---CRC的校验
			if ((USARTTask_CRCTask_Read(USARTx) == OK_0) && (USARTTask_DeviceID(USARTx) == OK_0))
			{
				//---数据发送报头
				USARTTask_RealTime_AddByte(USARTx, USARTx->msgTxID);

				//---数据接收完成
				if (pFuncTask != NULL)
				{
					pFuncTask(USARTx->msgRxHandler.pMsgVal, USARTx->msgTxHandler.pMsgVal);
				}
				else
				{
					//USARTTask_FillMode_AddData(USARTx, USARTx->msgRxHandler.pMsgVal, length);
					USARTTask_RealTime_AddByteSize(USARTx, USARTx->msgRxHandler.msgCount);
					for (length = USART1_ID_INDEX; length < USARTx->msgRxHandler.msgCount; length++)
					{
						USARTTask_RealTime_AddByte(USARTx, USARTx->msgRxHandler.pMsgVal[length]);
					}
				}

				//---判断是否发送CRC
				USARTTask_RealTime_AddByteCRC(USARTx);
			}
			else
			{
				//---发生CRC校验错误
				USART_Printf(USARTx, "=>>串口%d:发生CRC校验错误<<=\r\n", (USARTx->msgIndex - 1));
			}
			return USARTTask_ReadInit(USARTx);
		}
		return USARTTask_TimeOVFTask(USARTx);
	}
	return ERROR_2;
}